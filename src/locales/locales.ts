type DeepKeys<T, Prefix extends string = ""> = T extends string
  ? Prefix extends ""
    ? never
    : Prefix
  : T extends object
    ? {
        [K in keyof T]: K extends string ? DeepKeys<T[K], Prefix extends "" ? K : `${Prefix}.${K}`> : never;
      }[keyof T]
    : never;

